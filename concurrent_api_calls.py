#!/usr/bin/env python3
"""
Concurrent API calls to OpenAI-compatible API with caching and retry logic.

Usage:
    python concurrent_api_calls.py --model MODEL_NAME --base-url BASE_URL --input INPUT.jsonl [--output OUTPUT.jsonl]
    
    If --output is not provided, it will be generated by replacing .jsonl with _output.jsonl in the input path.
"""

import json
import argparse
import asyncio
from pathlib import Path
from typing import Dict, List, Optional, Any
from openai import AsyncOpenAI
import time


class APICaller:
    def __init__(self, model: str, base_url: str, cache_file: str, api_key: Optional[str] = None):
        self.model = model
        self.client = AsyncOpenAI(base_url=base_url, api_key=api_key or "dummy")
        self.cache_file = cache_file
        self.cache: Dict[str, int] = {}
        self.load_cache()
    
    def load_cache(self):
        """Load cached results from file."""
        if Path(self.cache_file).exists():
            try:
                with open(self.cache_file, 'r', encoding='utf-8') as f:
                    for line in f:
                        if line.strip():
                            item = json.loads(line)
                            # Use prompt as key for caching
                            prompt_key = json.dumps(item.get('prompt', ''), sort_keys=True)
                            self.cache[prompt_key] = self.cache.get(prompt_key, 0) + 1
                print(f"Loaded {len(self.cache)} cached results from {self.cache_file}")
            except Exception as e:
                print(f"Warning: Could not load cache: {e}")
                self.cache = {}
    
    def save_to_cache(self, result: Dict[str, Any]):
        """Save a single result to cache file (append mode)."""
        try:
            with open(self.cache_file, 'a', encoding='utf-8') as f:
                f.write(json.dumps(result, ensure_ascii=False) + '\n')
            prompt_key = json.dumps(result.get('prompt', ''), sort_keys=True)
            self.cache[prompt_key] = self.cache.get(prompt_key, 0) + 1
        except Exception as e:
            print(f"Warning: Could not save to cache: {e}")
    
    def cached_count(self, prompt: str) -> int:
        """Return the number of cached responses for a prompt."""
        prompt_key = json.dumps(prompt, sort_keys=True)
        return self.cache.get(prompt_key, 0)
    
    async def call_api(self, prompt: str, prompt_data: Dict[str, Any]) -> Optional[Dict[str, Any]]:
        """Make a single API call. Returns None on error."""
        try:
            # response = await self.client.chat.completions.create(
            #     model=self.model,
            #     messages=[
            #         {"role": "user", "content": prompt}
            #     ]
            # )
            
            # result = {
            #     **prompt_data,  # Include original data
            #     "prompt": prompt,
            #     "response": response.choices[0].message.content,
            #     "model": self.model,
            #     "timestamp": time.time()
            # }
            print(f"Calling API for prompt: {[prompt[:50]]}...")
            response = await self.client.responses.create(
                model=self.model,
                input=prompt,
                extra_body={"enable_thinking": True}
            )
            print(response)

            result = {
                **prompt_data,  # Include original data
                "prompt": prompt,
                "response": response.output_text,
                "model": self.model,
                "timestamp": time.time()
            }
            
            return result
        except Exception as e:
            print(f"Error calling API for prompt: {str(e)}")
            return None
    
    async def process_batch(self, prompts: List[tuple]) -> List[Optional[Dict[str, Any]]]:
        """
        Process a batch of prompts concurrently.
        Each item in prompts is (prompt_string, original_data_dict).
        """
        tasks = [self.call_api(prompt, data) for prompt, data in prompts]
        results = await asyncio.gather(*tasks)
        return results


def load_prompts(jsonl_file: str) -> List[Dict[str, Any]]:
    """Load prompts from JSONL file."""
    prompts = []
    with open(jsonl_file, 'r', encoding='utf-8') as f:
        for line in f:
            if line.strip():
                prompts.append(json.loads(line))
    return prompts


def extract_prompt(data: Dict[str, Any]) -> str:
    """Extract prompt from data. Assumes 'prompt' key or uses entire data as prompt."""
    if 'prompt' in data:
        prompt = data['prompt']
        if isinstance(prompt, str):
            return prompt
        else:
            return json.dumps(prompt, ensure_ascii=False)
    elif 'body' in data:  # For Stack Overflow questions
        return str(data['body'])
    else:
        # Use the entire data as context, convert to string
        return json.dumps(data, ensure_ascii=False)


async def main():
    parser = argparse.ArgumentParser(description='Make concurrent API calls with caching and retry logic')
    parser.add_argument('--model', required=True, help='Model name')
    parser.add_argument('--base-url', required=False, help='Base URL for OpenAI-compatible API', default=None)
    parser.add_argument('--input', required=True, help='Input JSONL file with prompts')
    parser.add_argument('--output', required=False, help='Output JSONL file for cached results (default: input path with _output.jsonl)')
    parser.add_argument('--api-key', help='API key (optional, can also use OPENAI_API_KEY env var)')
    parser.add_argument('--max-concurrent', type=int, default=10, help='Maximum concurrent requests (default: 10)')
    parser.add_argument('--beam-size', type=int, default=1, help='Number of responses per prompt (default: 1)')
    
    args = parser.parse_args()
    
    # Generate output path from input if not provided
    if not args.output:
        from datetime import datetime
        input_path = Path(args.input)
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        if input_path.suffix == '.jsonl':
            args.output = str(input_path.with_suffix('')) + f'_output_{timestamp}.jsonl'
        else:
            args.output = str(input_path) + f'_output_{timestamp}.jsonl'
    
    # Load prompts
    print(f"Loading prompts from {args.input}...")
    all_prompts_data = load_prompts(args.input)
    print(f"Loaded {len(all_prompts_data)} prompts")
    
    # Initialize API caller
    api_key = args.api_key or None
    caller = APICaller(args.model, args.base_url, args.output, api_key=api_key)
    
    # Expand prompts by beam size
    all_prompts_data = all_prompts_data * args.beam_size

    # Filter out already cached prompts by count
    remaining_prompts = []
    seen_counts: Dict[str, int] = {}
    for data in all_prompts_data:
        prompt = extract_prompt(data)
        prompt_key = json.dumps(prompt, sort_keys=True)
        seen_counts[prompt_key] = seen_counts.get(prompt_key, 0) + 1
        if seen_counts[prompt_key] <= caller.cached_count(prompt):
            print(f"Skipping cached prompt: {prompt[:50]}...")
        else:
            remaining_prompts.append((prompt, data))
    
    if not remaining_prompts:
        print("All prompts are already cached!")
        return
    
    print(f"Processing {len(remaining_prompts)} uncached prompts...")
    
    # Process in iterations
    iteration = 0
    while remaining_prompts:
        iteration += 1
        print(f"\n=== Iteration {iteration} ===")
        print(f"Remaining prompts: {len(remaining_prompts)}")
        
        # Process in a pool to limit concurrency without batch-level waiting
        max_concurrent = args.max_concurrent
        successful = 0
        failed = 0
        new_remaining = []
        
        semaphore = asyncio.Semaphore(max_concurrent)

        async def call_with_semaphore(prompt: str, data: Dict[str, Any]):
            async with semaphore:
                return prompt, data, await caller.call_api(prompt, data)

        tasks = [asyncio.create_task(call_with_semaphore(prompt, data))
                 for prompt, data in remaining_prompts]
        print(f"Processing {len(tasks)} prompts with max {max_concurrent} concurrent...")

        for completed in asyncio.as_completed(tasks):
            prompt, data, result = await completed
            if result is not None:
                caller.save_to_cache(result)
                successful += 1
            else:
                new_remaining.append((prompt, data))
                failed += 1
        
        remaining_prompts = new_remaining
        print(f"Iteration {iteration} complete: {successful} successful, {failed} failed")
        
        if remaining_prompts:
            print(f"Waiting before next iteration...")
            await asyncio.sleep(1)
    
    print(f"\n=== Complete ===")
    print(f"All prompts processed. Results saved to {args.output}")


if __name__ == "__main__":
    asyncio.run(main())

